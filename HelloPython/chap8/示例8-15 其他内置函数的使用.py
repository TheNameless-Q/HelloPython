#format
print(format(3.14,'20'))# 数信型默认右对产
print(format('hello','20'))#字符串默认左对齐
print(format('hello','*<20'))#<左对齐，*表示的填允符，20表示的是显示的宽度
print(format('hello','*>20'))
print(format('hello','*^20'))

print(format(3.1415926,'.2f'))
print(format(20,'b'))#二进制
print(format(20,'o'))#八进制
print(format(20,'x'))#十六进制
print(format(20,'X'))









